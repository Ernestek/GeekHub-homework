"""5. Напишіть функцію,яка приймає на
вхід рядок та повертає кількість окремих
регістро-незалежних букв та цифр, які
зустрічаються в рядку більше ніж 1 раз.
Рядок буде складатися лише з цифр та букв
(великих і малих). Реалізуйте обчислення за
допомогою генератора в один рядок"""


def counter(string):
    result = [char for char in set(string.lower()) if string.lower().count(char) > 1]
    return len(result)


print(counter("aabBcde111111"))
